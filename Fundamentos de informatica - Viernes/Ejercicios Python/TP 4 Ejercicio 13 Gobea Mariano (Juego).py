NumeroMes=int(input("Ingrese un numero de mes: "))
if NumeroMes==1:
    print("Enero")
elif NumeroMes==2:
    print("Febrero")
elif NumeroMes==3:
    print("Marzo")
elif NumeroMes==4:
    print("Abril")
elif NumeroMes==5:
    print("Mayo")
elif NumeroMes==6:
    print("Junio")
elif NumeroMes==7:
    print("Julio")
elif NumeroMes==8:
    print("Agosto")
elif NumeroMes==9:
    print("Septiembre")
elif NumeroMes==10:
    print("Octubre")
elif NumeroMes==11:
    print("Noviembre")
elif NumeroMes==12:
    print("Diciembre")
else:
    print("El numero ingresado es invalido")